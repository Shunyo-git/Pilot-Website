function todistrict(num) {
	document.forms[0].district.selectedIndex=0;
	for(ctr=0;ctr<cityarray[num].length;ctr++){
	document.forms[0].district.options[ctr] =new Option(cityarray[num][ctr], cityarray[num][ctr]);
	}
	document.forms[0].district.length=cityarray[num].length;
	
	if (document.forms[0].city.value == "105"){
            document.forms[0].zip_code0.value = 300;
            document.forms[0].district0.value = "新竹市";
	}
	if (document.forms[0].city.value == "301"){
	    document.forms[0].zip_code0.value = 600;
            document.forms[0].district0.value = "嘉義市";
	}
}

